// For View Positions
document.querySelectorAll('.job-card .btn').forEach(button => {
  button.setAttribute("data-bs-toggle", "modal");
  button.setAttribute("data-bs-target", "#jobModal");

  button.addEventListener('click', function () {
    const card = this.closest('.job-card');
    const title = card.querySelector('h5').innerText;

    // Collect data from objects
    const data = jobData[title];

    if (data) {
      document.getElementById('jobTitle').textContent = title;
      document.getElementById('jobEmployer').textContent = data.employer;
      document.getElementById('jobLocation').textContent = data.location;
      document.getElementById('jobGender').textContent = data.gender;
      document.getElementById('jobDescription').textContent = data.description;
      document.getElementById('jobQualifications').innerHTML = data.qualifications;

      //Apply button save
      document.getElementById('applyNowBtn').setAttribute("data-job", title);
    }
  });
});

// Apply Now button action
document.getElementById('applyNowBtn').addEventListener('click', function(){
  const jobTitle = this.getAttribute("data-job");

  // Redirect to application form with job title as query parameter
  window.location.href = "application-form.html?jobTitle=" + encodeURIComponent(jobTitle);
});